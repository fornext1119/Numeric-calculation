#import <Foundation/Foundation.h>

int main()
{
    printf("%d\n", 3 + 5);
    printf("%d\n", 3 - 5);
    printf("%d\n", 3 * 5);
    printf("%d\n", (int)pow(3, 5));
    printf("%d\n", 5 / 3);
    printf("%f\n", 5.0 / 3);
    printf("%f\n", 5 / 3.0);
    printf("%d\n", 5 % 3);

    printf("%3d\n",     3 * 5);
    printf("%23.20f\n", 5 / 3.0);

    return 0;
}
