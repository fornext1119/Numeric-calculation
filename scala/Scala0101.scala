object Scala0101 {
    def main(args:Array[String]) {
        println(3 + 5)
        println(3 - 5)
        println(3 * 5)
        println(Math.pow(3, 5))
        println(5 / 3)
        println(5.0 / 3)
        println(5 / 3.0)
        println(5 % 3)

        printf("%d\n", 3 * 5)
        print(3 * 5 + "\n")

        println("%3d".format(3 * 5))
        println("%23.20f".format(5 / 3.0))
    }
}
